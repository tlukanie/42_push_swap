#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	unsigned char	*ch;
	size_t			i;

	ch = s;
	i = 0;
	while (i < n)
	{
		ch[i] = '\0';
		i++;
	}
	return (s);
}
